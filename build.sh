#!/bin/bash
echo "<!-- Generated by: Lots of libs loader creator by TStudios. -->" > $PWD/lotsoflibs.html
echo -e "<!-- CSS -->" >> $PWD/lotsoflibs.html
i=0
function css {
  x=$(cat css.list)
  rm -rf css
  mkdir css
  cd css
  while read line; do
    y=`basename $line`
    wget -O $y --quiet $line
    echo -e "<!-- $y   SOURCE: $line -->\n<style>" >> $PWD/../lotsoflibs.html
    cat ./$y  >> $PWD/../lotsoflibs.html
    echo "</style>" >> $PWD/../lotsoflibs.html
    ((i++))
  done <<< "$x"
  cd ..
}
css
echo -e "\n\n<!-- JS -->" >> $PWD/lotsoflibs.html
function js {
  x=$(cat js.list)
  rm -rf js
  mkdir js
  cd js
  while read line; do
    y=`basename $line`
    wget -O $y --quiet $line
    echo -e "<!-- $y   SOURCE: $line -->\n<script>" >> $PWD/../lotsoflibs.html
    cat ./$y  >> $PWD/../lotsoflibs.html
    echo "</script>" >> $PWD/../lotsoflibs.html
    ((i++))
  done <<< "$x"
  cd ..
}
js
